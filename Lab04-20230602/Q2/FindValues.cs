using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Q2
{
    internal class FindValues
    {

        public double FindArea(double r)
        {
            double a = 3.14 * r * r;
            return a;
        }

        public double FindCircumference(double r)
        {
            double c = 2 * 3.14 * r;
            return c;
        }
    }
}
